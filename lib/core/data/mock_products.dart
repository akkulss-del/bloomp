  /// Получить случайные продукты
  static List<Product> getRandom(int count) {
    final shuffled = List<Product>.from(all)..shuffle();
    return shuffled.take(count).toList();
  }

  /// Поиск по категории
  static List<Product> getByCategory(String category) {
    return all.where((p) => p.category == category).toList();
  }

  /// Поиск по бренду
  static List<Product> getByBrand(String brand) {
    return all.where((p) => p.brand == brand).toList();
  }

  /// Рекомендации (популярные + в наличии)
  static List<Product> getRecommended() {
